-- Criação da tabela EMAIL
CREATE TABLE email (
  email_id        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  email_address   VARCHAR2(100),
  email_type      CHAR(1) NOT NULL,
  person_id       NUMBER NOT NULL,
  CONSTRAINT fk_email_type FOREIGN KEY (email_type) REFERENCES email_type(email_type),
  CONSTRAINT fk_person_email FOREIGN KEY (person_id) REFERENCES people(person_id)
);

